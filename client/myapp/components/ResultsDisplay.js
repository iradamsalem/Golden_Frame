import React, { useEffect, useState } from 'react';
import { View, Text, Image, StyleSheet, TouchableOpacity } from 'react-native';
import { useNavigation, useRoute } from '@react-navigation/native';

const ResultsDisplay = () => {
  const navigation = useNavigation();
  const route = useRoute();
  const { purpose } = route.params || {};

  const [imageUri, setImageUri] = useState(null);

  useEffect(() => {
    fetch('http://192.162.8.136:3001/api/photos/selected-image')
      .then((res) => res.json())
      .then((data) => {
        setImageUri(data.image); // image is base64 data URI
      })
      .catch((error) => {
        console.error('Error fetching selected image:', error);
      });
  }, []);

  return (
    <View style={styles.container}>
      <Text style={styles.title}>Your Perfect Photo</Text>

      <View style={styles.row}>
        <View style={styles.imageWrapper}>
          {imageUri ? (
            <Image
              source={{ uri: imageUri }}
              style={styles.image}
              resizeMode="contain"
            />
          ) : (
            <Text style={{ color: '#ccc', textAlign: 'center' }}>Loading image...</Text>
          )}
        </View>

        <View style={styles.feedbackWrapper}>
          <View style={styles.card}>
            <Text style={styles.cardTitle}>
              Why This Works for {purpose}
            </Text>
            <Text style={styles.cardText}>
              Feedback will appear here once it's generated by our AI engine.
            </Text>
          </View>

          <TouchableOpacity
            style={styles.button}
            onPress={() => navigation.navigate('UploadPhotos')}
          >
            <Text style={styles.buttonText}>Try Again with New Photos</Text>
          </TouchableOpacity>
        </View>
      </View>
    </View>
  );
};

const styles = StyleSheet.create({
  container: {
    padding: 20,
    backgroundColor: '#0a0a23',
    borderRadius: 10,
    flex: 1,
  },
  title: {
    fontSize: 28,
    fontWeight: 'bold',
    marginBottom: 40,
    color: 'gold',
    paddingTop: 40,
    textAlign: 'center',
  },
  row: {
    flexDirection: 'column',
  },
  imageWrapper: {
    marginBottom: 20,
    backgroundColor: '#1a1a2e',
    padding: 8,
    borderRadius: 10,
  },
  image: {
    width: '100%',
    height: 300,
    borderRadius: 10,
  },
  feedbackWrapper: {
    gap: 12,
  },
  card: {
    backgroundColor: '#2b2b3c',
    padding: 16,
    borderRadius: 10,
  },
  cardTitle: {
    fontSize: 18,
    color: 'gold',
    fontWeight: '600',
    marginBottom: 8,
  },
  cardText: {
    color: '#ddd',
    fontSize: 14,
  },
  button: {
    marginTop: 20,
    alignSelf: 'center',
    backgroundColor: 'gold',
    paddingVertical: 10,
    paddingHorizontal: 20,
    borderRadius: 25,
  },
  buttonText: {
    color: '#1a1a2e',
    fontWeight: 'bold',
    fontSize: 16,
  },
});

export default ResultsDisplay;
